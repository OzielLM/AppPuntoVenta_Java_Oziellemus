    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) tblDatos.getModel();
        int row = tblDatos.getSelectedRow();
        if(Integer.parseInt(txtCantidad.getText()) <= Integer.parseInt(model.getValueAt(row, 3).toString())){
            formPrincipal.traerDatos(model.getValueAt(row,0) + " " + txtCantidad.getText() + " " + model.getValueAt(row, 2));
            for (int i = 0; i < Negocio.NegocioProducto.lstProductos.size(); i++) {
                if(Negocio.NegocioProducto.lstProductos.get(i).getNombreProducto().contains(model.getValueAt(row, 0).toString())){
                    Negocio.NegocioProducto.lstProductos.get(i).setCantidadProducto(Negocio.NegocioProducto.lstProductos.get(i).getCantidadProducto() - Integer.parseInt(txtCantidad.getText()));
                }
            }
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Escogio una cantidad mayor a la que se encuentran en el inventario");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

